import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import Database from 'better-sqlite3';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const defaultDbPath = path.resolve(__dirname, '../../../data/atlas.db');
const dbPath = path.resolve(process.cwd(), process.argv[2] ?? defaultDbPath);

const tables = [
  {
    name: 'properties',
    columns: [
      'id',
      'name',
      'city',
      'group_name',
      'ical_url',
      'timezone',
      'is_active',
      'sort_order',
      'last_synced_at',
      'last_sync_status',
      'last_sync_error',
      'created_at',
      'updated_at',
    ],
  },
  {
    name: 'calendar_events',
    columns: [
      'id',
      'property_id',
      'external_uid',
      'event_type',
      'starts_on',
      'ends_on',
      'summary',
      'raw_status',
      'last_synced_at',
      'created_at',
      'updated_at',
    ],
  },
  {
    name: 'sync_runs',
    columns: [
      'id',
      'property_id',
      'status',
      'started_at',
      'finished_at',
      'event_count',
      'error_message',
    ],
  },
];

if (!fs.existsSync(dbPath)) {
  console.error(`SQLite database not found at ${dbPath}`);
  process.exit(1);
}

function sqlValue(value) {
  if (value === null || value === undefined) return 'NULL';
  if (typeof value === 'number') return Number.isFinite(value) ? String(value) : 'NULL';
  if (typeof value === 'bigint') return value.toString();
  if (typeof value === 'boolean') return value ? '1' : '0';
  if (value instanceof Uint8Array) {
    return `X'${Buffer.from(value).toString('hex')}'`;
  }
  return `'${String(value).replace(/'/g, "''")}'`;
}

const db = new Database(dbPath, { readonly: true });

try {
  const counts = Object.fromEntries(
    tables.map(({ name }) => [name, db.prepare(`SELECT COUNT(*) AS count FROM ${name}`).get().count])
  );

  const lines = [
    '-- Generated by apps/api/scripts/export-sqlite-to-d1.mjs',
    `-- Source database: ${dbPath}`,
    `-- properties: ${counts.properties}`,
    `-- calendar_events: ${counts.calendar_events}`,
    `-- sync_runs: ${counts.sync_runs}`,
    'DELETE FROM calendar_events;',
    'DELETE FROM sync_runs;',
    'DELETE FROM properties;',
  ];

  for (const table of tables) {
    const rows = db
      .prepare(`SELECT ${table.columns.join(', ')} FROM ${table.name} ORDER BY id`)
      .all();

    if (!rows.length) continue;

    lines.push(`-- ${table.name}`);
    for (const row of rows) {
      const values = table.columns.map((column) => sqlValue(row[column])).join(', ');
      lines.push(`INSERT INTO ${table.name} (${table.columns.join(', ')}) VALUES (${values});`);
    }
  }
  process.stdout.write(`${lines.join('\n')}\n`);
} finally {
  db.close();
}
